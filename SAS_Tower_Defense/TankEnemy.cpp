void TankEnemy::move() {
    findNextPosition();
}

void TankEnemy::findNextPosition() {
    for (int y = currentY; y < path.size(); ++y) {
        for (int x = (y == currentY ? currentX + 1 : 0); x < path[y].size(); ++x) {
            if (path[y][x] == 1) {
                currentX = x;
                currentY = y;
                return;
            }
        }
    }
}

void TankEnemy::Render(sf::RenderWindow* window) {
    sprite.setPosition(currentX * 32, currentY * 32);
    window->draw(sprite);
}

void TankEnemy::Update() {
    move();
}